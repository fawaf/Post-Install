# Autojump
# https://github.com/joelthelion/autojump
# Autojump is a utility to complement cd by analyzing your directory
# travelling patterns to know where you travel most often. With autojump
# you can easily travel to locations that you commonly move to
sudo apt-get install autojump
echo "source /usr/share/autojump/autojump.bash" >> ~/.bashrc
